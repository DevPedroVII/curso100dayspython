 #Html

 # HTML (HyperText Markup Language)
 # é o bloco de construção mais básico da Web.
 # Ele define o significado e a estrutura do conteúdo da web.

 #HTML usa "marcação" para anotar texto,
 # imagens e outros conteúdos para exibição em um navegador da Web.
 # SA marcação HTML inclui "elementos" especiais como <head>,
 # <title>, <body>, <header>, <footer>, <article>, <section>,
 # <p>, <div>, <span>, <img>, <aside>, <audio>, <canvas>,
 # <datalist>, <details>, <embed>, <nav>, <output>, <progress>,
 # <video>, <ul>, <ol>e <li>muitos outros.


# for more https://developer.mozilla.org/en-US/docs/Web/HTML
