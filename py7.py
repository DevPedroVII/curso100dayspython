#complex()
#O método complex() retorna um número complexo quando partes reais
#e imaginárias são fornecidas ou converte uma string em um número complexo.

#A sintaxe de complex()é:
#complex([real[, imagem]])

#Em geral, o complex()método recebe dois parâmetros:
#real - parte real. Serealfor omitido, o padrão é 0.
#imagem - parte imaginária. Seimagemfor omitido, o padrão é 0.
#Se o primeiro parâmetro passado para este método for uma string,
#ela será interpretada como um número complexo.
#Neste caso, o segundo parâmetro não deve ser passado.
